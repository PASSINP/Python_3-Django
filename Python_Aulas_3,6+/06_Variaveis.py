# Uma variavel é algo que salvamos na memoria no computador. Ela armazena uma
# inofrmação que pode ser chamada depois. Podendo ser algo simples como um texto
# ou até mesmo algo mais complicado como uma função 

# Para criar um variavel apenas delcare seu nome, coloque igual e em seguida o 
# que você deseja colocar dentro da variavel
nome = "Paulo"
print(nome)

# É possivel ver a classe de uma variavel
print(type(nome))

# Como dito anteriormente é possivel criar uma variavel com alguma função
# Não é necessario utilizar espaço na variavel entre o seu nome e o que ela faz.
#Isso é feito somente por aparencia
soma=2+2
print(soma)

# Variaveis podem ser alterada ao longo do caminho em Python.
nome = "João"
print(nome)