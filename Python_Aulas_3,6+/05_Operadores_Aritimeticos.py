"""
+ _ Adição
- _ Subtração
* _ Multiplicação
/ _ Divisão
// _ Divisão com resultado arredondado
** _ Potenciação
% _ Modulo/Resto da divisão
() - Ordem de precedência
"""

print(20 + 20) # + _ Adição
print(20 - 20) # - _ Subtração
print(20 * 20) # * _ Multiplicação
print(20 / 17) # / _ Divisão
print(20 // 14) # // _ Divisão com resultado arredondado
print(20 ** 20) # ** _ Potenciação
print(20 % 16) # % _ Modulo/Resto da divisão